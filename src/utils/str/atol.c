long	ft_atol(char *s)
{
	int		i;
	int		neg;
	long	res;

	i = -1;
	neg = 0;
	res = 0;
	while (s[++i] == '+' || s[i] == '-')
		if (s[i] == '-')
			neg = !neg;
	while (s[i])
		res = (res * 10) + (s[i++] - '0');
	if (!neg)
		return (res);
	return (res * -1);
}
